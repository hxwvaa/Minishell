#include "libft.h"

long long	ft_atol(const char *str)
{
	long long	nb;
	int					sign;

	sign = 1;
	nb = 0;
	while (*str == ' ' || (*str >= '\t' && *str <= '\r'))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + *str - '0';
		str++;
	}
	if ((sign * nb) == 	LONG_MIN)
		return(LONG_MIN);
	return (sign * nb);
}
